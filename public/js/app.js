// Tracker: dialect → current sentence index
// Load from localStorage or start fresh
let currentProgress = JSON.parse(localStorage.getItem("progress")) || {};

// Save progress
function saveProgress() {
  localStorage.setItem("progress", JSON.stringify(currentProgress));
}

// Get next serial sentence for a dialect
function getNextSentence(dialect) {
  if (!currentProgress[dialect]) currentProgress[dialect] = 1; // start at 1

  if (currentProgress[dialect] > sentences.length) {
    return { id: null, text: "এই উপভাষার জন্য সব বাক্য রেকর্ড সম্পন্ন হয়েছে।" };
  }

  const sentence = sentences.find(s => s.id === currentProgress[dialect]);

  // Increment and save
  currentProgress[dialect] += 1;
  saveProgress();

  return sentence;
}

// Reset helpers
function resetDialect(dialect) {
  currentProgress[dialect] = 1;
  saveProgress();
}
function resetAll() {
  currentProgress = {};
  saveProgress();
}

// UI elements
const sentenceEl = document.getElementById("currentSentence");
const startBtn = document.getElementById("startBtn");
const stopBtn = document.getElementById("stopBtn");
const statusEl = document.getElementById("status");
const dialectSel = document.getElementById("dialect");
const floating = document.getElementById("recordingBox");

// Recording state
let mediaRecorder, audioChunks = [];
let currentSentence = null;
let currentDialect = "";

// Start Recording
startBtn.onclick = async () => {
  currentDialect = dialectSel.value;
  if (!currentDialect) { alert("প্রথমে একটি উপভাষা নির্বাচন করুন"); return; }

  // Pick next serial sentence
  currentSentence = getNextSentence(currentDialect);
  sentenceEl.textContent = currentSentence.text;

  if (!currentSentence.id) {
    startBtn.disabled = true;
    stopBtn.disabled = true;
    return;
  }

  const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
  const mime = MediaRecorder.isTypeSupported("audio/webm;codecs=opus") ? "audio/webm;codecs=opus" :
               (MediaRecorder.isTypeSupported("audio/webm") ? "audio/webm" : "");
  mediaRecorder = new MediaRecorder(stream, mime ? { mimeType: mime } : undefined);

  audioChunks = [];
  mediaRecorder.ondataavailable = e => { if (e.data.size) audioChunks.push(e.data); };

  mediaRecorder.onstop = async () => {
    if (currentSentence && currentSentence.id) {
      const blob = new Blob(audioChunks, { type: mime || "application/octet-stream" });

      // filename pattern: dialect_sentenceID.wav
      const filename = `${currentDialect}_${currentSentence.id}.wav`;

      const form = new FormData();
      form.append("file", blob, filename);
      form.append("dialect", currentDialect);
      form.append("sentence_id", String(currentSentence.id));
      form.append("sentence_text", currentSentence.text);

      try {
        const res = await fetch(`${BACKEND_URL}/api/upload`, { method: "POST", body: form });
        const data = await res.json();
        if (res.ok && data.success) {
          statusEl.textContent = `সংরক্ষণ সম্পন্ন হয়েছে: ${data.filename}`;
        } else {
          statusEl.textContent = `সংরক্ষণ ব্যর্থ: ${data.error || 'unknown'}`;
        }
      } catch (err) {
        console.error(err);
        statusEl.textContent = "সার্ভারের সাথে সংযোগ ব্যর্থ হয়েছে।";
      }
    }

    // Hide until next record
    sentenceEl.textContent = "রেকর্ড শেষ হয়েছে। নতুন বাক্য আসবে।";
    floating.classList.add("hidden");
    startBtn.disabled = false;
    stopBtn.disabled = true;
  };

  mediaRecorder.start();
  statusEl.textContent = `রেকর্ড চলছে... বাক্য ${currentSentence.id} পড়ুন।`;
  floating.classList.remove("hidden");
  startBtn.disabled = true;
  stopBtn.disabled = false;
};

// Stop Recording
stopBtn.onclick = () => {
  if (mediaRecorder && mediaRecorder.state !== "inactive") mediaRecorder.stop();
  statusEl.textContent = "রেকর্ড শেষ হয়েছে।";
};
